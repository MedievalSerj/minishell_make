#include "libft.h"

t_lst	*l_get_nth(t_lst *head, int n)
{
	int		i;
	t_lst	*tmp;

	i = 0;
	tmp = head;
	while (i < n && tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (tmp);
}
